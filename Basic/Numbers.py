#1Int
x = 10
y = -5
z = x * y
print("Multipplication of", x, "and", y, "is", z)

#2Float
pi = 3.14159
radius = 7.0
area = pi * (radius ** 2)
print("Area of circle with radius", radius, "is", area)

#floating point error
a = 0.1 + 0.2
print(a)  # Output: 0.30000000000000004
#Rounding
print(round(a, 2))  # Output: 0.3

#3Complex
x1 = 3 + 4j
